public class MathDemo_34 {

	public static void main(String[] args) {
	
		System.out.println(Math.ceil(20.5)); //(天花板)進一 
		System.out.println(Math.floor(20.5)); //(地板)去尾
		
		System.out.println(Math.ceil(-20.5)); //去尾
		System.out.println(Math.floor(-20.5)); //進一
	}
}
